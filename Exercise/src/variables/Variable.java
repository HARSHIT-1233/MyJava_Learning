package variables;

public class Variable {

    public static void main(String[] args){

        /* either do this
        int a = 10;
        int b = 20;
        int c = a + b;
        System.out.println("c = " +c);
        */

        // or this
        int a = 10, b = 20, c = a + b;
        System.out.println("c = " +c);

        /* Variable Naming Conventions
        *  int age; // valid
        *  int _age; // valid
        *  int $age; // valid
        *  int age3; // valid
        *  int a ge; // invalid
        *  int 3age; // invalid
        */
    }

}
